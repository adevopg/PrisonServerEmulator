  delito_id    INT UNSIGNED NOT NULL,
  habilidad_id INT UNSIGNED NOT NULL,
  maximo       TINYINT UNSIGNED NOT NULL DEFAULT 100, -- "Puntos Máximos" del manual (cliente x1.5)
